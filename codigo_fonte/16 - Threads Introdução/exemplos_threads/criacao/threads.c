/*--------------------------------------------------------------*/
#include <stdio.h>
#include <pthread.h>

/*--------------------------------------------------------------*/
#define N 10000
/*--------------------------------------------------------------*/
void *funcao(){
	return;
}

/*--------------------------------------------------------------*/
int main(){
	int i;
	pthread_t id;

	for( i=0; i<N; i++){
	
		pthread_create(&id,NULL,funcao,NULL);

		pthread_join(id,NULL);
	}
}
